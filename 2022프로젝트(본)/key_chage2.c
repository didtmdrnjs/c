#include<stdio.h>

void key_chage2(int* key)
{
	switch (*key)
	{
	case 0x41: *key = 1; break;
	case 0x42: *key = 2; break;
	case 0x43: *key = 3; break;
	case 0x44: *key = 4; break;
	case 0x45: *key = 5; break;
	case 0x46: *key = 6; break;
	case 0x47: *key = 7; break;
	case 0x48: *key = 8; break;
	case 0x49: *key = 9; break;
	case 0x4A: *key = 10; break;
	case 0x4B: *key = 11; break;
	case 0x4C: *key = 12; break;
	case 0x4D: *key = 13; break;
	case 0x4E: *key = 14; break;
	case 0x4F: *key = 15; break;
	case 0x50: *key = 16; break;
	case 0x51: *key = 17; break;
	case 0x52: *key = 18; break;
	case 0x53: *key = 19; break;
	case 0x54: *key = 20; break;
	case 0x55: *key = 21; break;
	case 0x56: *key = 22; break;
	case 0x57: *key = 23; break;
	case 0x58: *key = 24; break;
	case 0x59: *key = 25; break;
	case 0x5A: *key = 26; break;
	}
}